# CONCEPT 10: PYTEST GATES FOR METRICS
# =============================================================================
"""
Your existing project already has this pattern:

    testing-framework/tests/helpers/ragas_assertions.py

Concept:

    evaluate dataset with metric
    convert results to pandas
    assert score >= threshold

Example threshold gates:

    faithfulness >= 0.70
    answer_relevancy >= 0.75
    context_precision >= 0.60
    context_recall >= 0.60


Simple pytest pattern:

    def test_faithfulness_above_threshold(eval_df):
        assert eval_df["faithfulness"].mean() >= 0.70


Better pytest pattern:

    def test_each_answer_faithfulness_above_threshold(eval_df):
        failing = eval_df[eval_df["faithfulness"] < 0.70]
        assert failing.empty, failing[["question", "response", "faithfulness"]]


Why per-row checks are better:
    Average can hide bad individual failures.

Example:
    9 answers score 1.0
    1 answer scores 0.0
    average = 0.90

The average looks good, but one user got a fully hallucinated answer.
"""


# =============================================================================
# CONCEPT 11: SMALL MANUAL FORMULA EXAMPLE
# =============================================================================
"""
Suppose we evaluate 10 safety prompts.

Actual unsafe prompts:
    4

Actual safe prompts:
    6

Model blocked:
    5 prompts

Breakdown:
    TP = 3  unsafe correctly blocked
    FP = 2  safe incorrectly blocked
    FN = 1  unsafe missed
    TN = 4  safe allowed

Accuracy:
    (TP + TN) / total = (3 + 4) / 10 = 0.70

Precision:
    TP / (TP + FP) = 3 / (3 + 2) = 0.60

Recall:
    TP / (TP + FN) = 3 / (3 + 1) = 0.75

F1:
    2 * (0.60 * 0.75) / (0.60 + 0.75)
    = 0.67

False positive rate:
    FP / (FP + TN) = 2 / (2 + 4) = 0.33

False negative rate:
    FN / (FN + TP) = 1 / (1 + 3) = 0.25

Interpretation:
    Recall is decent, but precision is low. The system blocks too many safe
    prompts. This is the over-blocking issue you saw in Block 2.
"""


