# =============================================================================
# CHECKPOINT QUESTIONS
# =============================================================================
"""
1. What is the difference between faithfulness and answer relevancy?

Faithfulness checks whether the answer is supported by the retrieved context or
tool result. Answer relevancy checks whether the answer addresses the user's
question. An answer can be faithful but irrelevant if it is grounded in context
but answers the wrong thing.


2. What does low context recall mean?

Low context recall means retrieval missed important information required to
answer the question. This often causes hallucination because the model may fill
missing gaps from its own knowledge.


3. What does low context precision mean?

Low context precision means the retriever returned noisy or irrelevant chunks.
This can distract the model and lead to mixed, incorrect, or irrelevant answers.


4. How do you detect hallucination after a tool failure?

Check whether the final answer is grounded in the tool result. If the tool
returns success=False or "Customer not found", the final answer must not invent
customer details, ticket ids, prices, statuses, or calculations.


5. How would you explain this block in an interview?

Interview answer:

    "For AI evaluation, I split quality into retrieval, generation, tool, and
    safety metrics. For retrieval, I track context precision and context recall.
    For generation, I track faithfulness, groundedness, answer relevancy, and
    hallucination rate. For agents, I add tool selection accuracy, argument
    accuracy, trajectory accuracy, and tool-result grounding. I use dashboards to
    diagnose whether a failure came from retrieval, model generation, tool
    execution, or guardrails, and I convert stable thresholds into pytest gates."
"""
